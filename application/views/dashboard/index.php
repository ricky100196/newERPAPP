  value: 1322
}, {
  country: "Mei",
  value: 1122
}, {
  country: "Juni",
  value: 1114
}, {
  country: "Juli",
  value: 984
}, {
  country: "Agustus",
  value: 711
}, {
  country: "September",
  value: 665
}, {
  country: "Oktober",
  value: 443
}, {
  country: "November",
  value: 441
},{
  country: "Desember",
  value: 450
}

];

xAxis.data.setAll(data);
series.data.setAll(data);


// Make stuff animate on load
// https://www.amcharts.com/docs/v5/concepts/animations/
series.appear(1000);
chart.appear(1000, 100);

}); // end am5.ready()
</script>